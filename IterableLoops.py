 
print("Iterate in Strings")
for char in "loops":
    print(char, end=' ')

print("\n")

print("Iterate in Tuples")
for num in (1,2,3,4):
    print(num, end=' ')

print("\n")

print("Iterate in Sets") #Sets are unordered sequences
for num in {1,2,3,4}:
    print(num, end=' ')


print("\n")

print("Iterate in Dictionary") 
for key in {"a":1, "b":2, "c":3}:
    print(key, end=' ')

 